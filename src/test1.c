uint8_t inbuf[256];
uint8_t outbuf[256];

int test_case(Demulti2Handle h, uint8_t *inb, int len, uint8_t *outb,
	      const char * const msg)
{
	int ret;

	memcpy(inb, body, sizeof(body));
	fprintf(stderr, "descrambling (%s)...", msg);
	fflush(stderr);
	ret = demulti2_descramble(h, inb, len, sc, ecm_pid, outb);
	if (ret) {
		fprintf(stderr, "\x1b[31mFAILED.  ret:%d\x1b[m\n", ret);
		return 2;
	}
	if (!outb)
		outb = inb;
	if (memcmp(outb, ans, len)) {
		fprintf(stderr, "\x1b[31mBAD\x1b[m\n");
		return 1;
	}
	fprintf(stderr, "\x1b[32mOK\x1b[m\n");
	return 0;
}

void time_case(Demulti2Handle h, uint8_t *inb, int len, uint8_t *outb,
		const char * const msg)
{
	int i;
	struct timespec t1, t2;

	if (!outb)
		outb = inb;

	memcpy(inb, body, sizeof(body));
	fprintf(stderr, "timing (%s)... ", msg);
	fflush(stderr);

	clock_gettime(CLOCK_PROCESS_CPUTIME_ID, &t1);
	for (i = 0; i < 1000000; i++)
		demulti2_descramble(h, inb, len, sc, ecm_pid, outb);

	clock_gettime(CLOCK_PROCESS_CPUTIME_ID, &t2);

	if (t2.tv_nsec < t1.tv_nsec) {
		t2.tv_sec --;
		t2.tv_nsec += 1000000000;
	}
	t2.tv_nsec -= t1.tv_nsec;
	t2.tv_nsec /= 1000000;
	t2.tv_sec -= t1.tv_sec;

	fprintf(stderr, "%gs\n", t2.tv_sec + t2.tv_nsec / 1000.0);
	return;
}
 
int main(void)
{
	Demulti2Handle h;
	int ret, l;
	uint8_t *p, *q;

	h = demulti2_open();
	if (!h) {
		fprintf(stderr, "demulti2_open() failed.\n");
		return 1;
	}

	ret = demulti2_feed_ecm(h, ecm, sizeof(ecm), ecm_pid);
	if (ret) {
		fprintf(stderr, "demulti2_feed_ecm() failed. ret:%d\n", ret);
		goto bailout;
	}

	p = (uint8_t *)(((uint64_t)inbuf + 31) & ~0x1FULL);
	q = (uint8_t *)(((uint64_t)outbuf + 31) & ~0x1FULL);
	l = sizeof(body);
	ret |= test_case(h, p, l, q, "32B aligned in, 16B aligned out");
	ret |= test_case(h, p, l, q + 1, "32B aligned in, un-aligned out");
	ret |= test_case(h, p + 16, l, q, "16B[32n+16] aligned in, 16B aligned out");
	ret |= test_case(h, p + 8, l, q, "8B[32n+8] aligned in, 16B aligned out");
	ret |= test_case(h, p + 24, l, q, "8B[32n+24] aligned in, 16B aligned out");
	ret |= test_case(h, p + 1, l, q, "un-aligned in/out");

	ret |= test_case(h, p, l, NULL, "32B aligned, in-place");
	ret |= test_case(h, p+1, l, NULL, "un-aligned in/out, in-place");

	if (!ret) {
		time_case(h, p, l, q, "in/32B, out/32B");
		time_case(h, p, l, q + 1, "in/32B, out/--");
		time_case(h, p + 1, l, q + 1, "in/--, out/--");
		time_case(h, p + 1, l, NULL, "in-place/--");
	}

bailout:
	demulti2_close(h);
	return ret;
}
